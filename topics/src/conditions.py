# Example if
x = 10

if x > 5:
    print("x is greater than 5")


# Example if-else
y = 3

if y % 2 == 0:
    print("y is even")
else:
    print("y is odd")


# Example if-elif-else
grade = 75

if grade >= 90:
    print("A")
elif grade >= 80:
    print("B")
elif grade >= 70:
    print("C")
else:
    print("F")


# Example with User Input
user_input = int(input("Enter a number: "))

if user_input > 0:
    print("The number is positive.")
elif user_input < 0:
    print("The number is negative.")
else:
    print("The number is zero.")